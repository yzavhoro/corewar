long	ft_atoi(char *str)
{
	long		por;
	long		ret;

	ret = 0;
	por = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			por = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str)
	{
		if ((ret > 922337203685477580 || (ret == 922337203685477580
			&& (*str - 48) > 7)) && por == 1)
			return (-1);
		if ((ret > 922337203685477580 || (ret == 922337203685477580
			&& (*str - 48) > 8)) && por == -1)
			return (0);
		ret = ret * 10 + (long)(*str - 48);
		str++;
	}
	return (ret * por);
}
