char	*ft_strcat(char *dest, const char *from)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i])
		i++;
	while (from[j])
	{
		dest[i + j] = from[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
