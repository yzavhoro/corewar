#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*temp;

	while (*alst)
	{
		temp = *alst;
		*alst = (*alst)->next;
		ft_lstdelone(&temp, del);
	}
}
