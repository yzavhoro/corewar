char	*ft_strrchr(const char *s, int c)
{
	char	*find;

	find = 0;
	if (s)
	{
		while (*s)
		{
			if (*s == (char)c)
				find = (char*)s;
			s++;
		}
		if (c == 0)
			return ((char*)s);
	}
	return (find);
}
